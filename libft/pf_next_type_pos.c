#include "libft.h"

int		pf_next_type_pos(char *s, int start)
{
	int		i;

	i = start + 1;
	while (pf_is_type(s[i]) != 1 && s[i] != '\0' && s[i] != '%')
	{
		if (!(s[i] >= '0' && s[i] <= '9') && s[i] != ' ' && s[i] != '.' &&
			s[i] != '#' && s[i] != '-' && s[i] != '+' && s[i] != 'h' &&
			s[i] != 'l' && s[i] != 'j' && s[i] != 'z')
			break ;
		i++;
	}
	return (i);
}
