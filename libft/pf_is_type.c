int		pf_is_type(char c)
{
	int		i;
	char	*type;

	i = 0;
	type = "sSpdDioOuUxXcC";
	while (type[i] != '\0')
	{
		if (type[i] == c)
			return (1);
		i++;
	}
	return (-1);
}
