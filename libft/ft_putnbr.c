#include "libft.h"

void	ft_putnbr(int n)
{
	int		arr[10];
	int		i;

	i = 0;
	if (n == 0)
		ft_putchar('0');
	if (n == -2147483648)
	{
		arr[0] = 8;
		n = n / 10;
		i++;
	}
	if (n < 0)
	{
		ft_putchar('-');
		n = n * -1;
	}
	while (n > 0)
	{
		arr[i++] = n % 10;
		n = n / 10;
	}
	while (--i >= 0)
		ft_putchar(arr[i] + '0');
}
